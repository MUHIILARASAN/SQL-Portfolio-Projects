 -- Q3. What is the average age of customers who can receive marketing emails (can_email is set to 'yes')?
select * from customer_insights_project.customers;

select avg(age) as avg_age
from customer_insights_project.customers
where can_email = 'yes';

-- Q4 How many orders were made by customers who aged 30 or older?

select * from customer_insights_project.customers;
select * from customer_insights_project.orders;

select count(distinct(o.order_id)) as total_orders
from customer_insights_project.customers c
inner join customer_insights_project.orders o 
on c.customer_id = o.customer_id
where c.age >30;

-- Q5 what is the total revenue generated by each product category?

select * from customer_insights_project.products;
select * from customer_insights_project.baskets;

select p.category, sum(p.price) as total_revenue_by_category
from products p 
left join baskets b on p.product_id = b.product_id
group by p.category;

